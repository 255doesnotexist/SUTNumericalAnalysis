 f = @(x)abs(x);
x = [  -5.0 -2.5  0.0 2.5  5.0] %% nodes


w_0=@(x)(x-0).*(x-5)/((-5-0).*(-5-5))
w_1=@(x)(x-(-5)).*(x-5)/((0-(-5)).*(0-5))
w_2=@(x)(x-(-5)).*(x-0)/((5-(-5)).*(5-0))

W=@(x) f(-5).*w_0(x) + f(0).*w_1(x) + f(5).*w_2(x);

plot(-5:0.1:5, f(-5:0.1:5));
hold on
plot(-5:0.1:5, W(-5:0.1:5));
